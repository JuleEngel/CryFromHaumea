def parse_cave(cave_str):
    """Parse cave string into a set of wall cell coordinates."""
    walls = set()
    lines = cave_str.strip().split("\n")
    # Center the cave around origin
    height = len(lines)
    width = max(len(line) for line in lines)
    offset_x = width // 2
    offset_y = height // 2

    for row, line in enumerate(lines):
        for col, ch in enumerate(line):
            if ch == "#":
                walls.add((col - offset_x, row - offset_y))

    return walls


def get_atlas_coords(cx, cy, walls):
    """Determine which atlas tile to use based on neighbors."""
    has_top = (cx, cy - 1) in walls
    has_right = (cx + 1, cy) in walls
    has_bottom = (cx, cy + 1) in walls
    has_left = (cx - 1, cy) in walls

    index = has_top * 1 + has_right * 2 + has_bottom * 4 + has_left * 8
    atlas_x = index % 4
    atlas_y = index // 4
    return atlas_x, atlas_y


def encode_tile(cx, cy, source_id, atlas_x, atlas_y, alternative=0):
    """Encode a tile as 12 bytes for TileMapLayer tile_map_data."""
    return struct.pack(
        "<hhHHHH",
        cx, cy, source_id, atlas_x, atlas_y, alternative,
    )


def main():
    walls = parse_cave(CAVE)
    print(f"Total wall tiles: {len(walls)}")

    # Find bounds
    min_x = min(x for x, y in walls)
    max_x = max(x for x, y in walls)
    min_y = min(x for x, y in walls)
    max_y = max(y for x, y in walls)
    print(f"Bounds: x=[{min_x}, {max_x}], y=[{min_y}, {max_y}]")

    # Encode all tiles
    data = bytearray()
    for cx, cy in sorted(walls):
        ax, ay = get_atlas_coords(cx, cy, walls)
        data.extend(encode_tile(cx, cy, 0, ax, ay))

    # Output as comma-separated bytes for .tscn
    byte_str = ", ".join(str(b) for b in data)
    print(f"\nPackedByteArray({byte_str})")

    # Also write to a file for easy copy-paste
    import os

    out_path = os.path.join(os.path.dirname(__file__), "cave_tile_data.txt")
    with open(out_path, "w") as f:
        f.write(f"PackedByteArray({byte_str})")
    print(f"\nWritten to {out_path}")


if __name__ == "__main__":
    main()
